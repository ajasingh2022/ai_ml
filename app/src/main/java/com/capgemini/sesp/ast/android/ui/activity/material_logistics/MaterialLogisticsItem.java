package com.capgemini.sesp.ast.android.ui.activity.material_logistics;


class MaterialLogisticsItem {
	private String name;
	private Integer iconid;
	private String secondLine;
	
	public MaterialLogisticsItem(String name, Integer iconid) {
        this.name = name;
        this.iconid = iconid;
    }
	
	public MaterialLogisticsItem(String name, Integer iconid, String secondLine) {
		this.name = name;
		this.iconid = iconid;
		this.secondLine = secondLine;
	}
	
	public String getName() {
		return name;
	}
	
	public Integer getIconid() {
		return iconid;
	}
	
	public String getSecondLine() {
	    return secondLine;
	}
	
}

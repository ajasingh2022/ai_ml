package com.capgemini.sesp.ast.android.ui.activity.dashboard;


public class MainMenuItem {
	private String name;
	private Integer iconid;
	
	public MainMenuItem(String name, Integer iconid) {
		this.name = name;
		this.iconid = iconid;
	}
	
	public String getName() {
		return name;
	}
	
	public Integer getIconid() {
		return iconid;
	}
	
}
